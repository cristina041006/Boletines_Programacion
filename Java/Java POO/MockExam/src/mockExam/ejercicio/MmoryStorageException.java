package mockExam.ejercicio;

public class MmoryStorageException extends Exception {

	public MmoryStorageException() {
	}

	public MmoryStorageException(String message) {
		super(message);
	}

	public MmoryStorageException(Throwable cause) {
		super(cause);
	}

	public MmoryStorageException(String message, Throwable cause) {
		super(message, cause);
	}

	public MmoryStorageException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
